/// <summary>
/// The range of an ability.
/// </summary>
public enum AbilityRange
{
    /// <summary>
    /// Affects a target within melee range.
    /// </summary>
    Melee,

    /// <summary>
    /// Affects a target at a distance.
    /// </summary>
    Ranged,

    /// <summary>
    /// Affects the self.
    /// </summary>
    Self
}