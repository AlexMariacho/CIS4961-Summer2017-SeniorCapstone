/// <summary>
/// Action taken when entity button is clicked.
/// </summary>
public enum ButtonAction
{
    /// <summary>
    /// No action taken.
    /// </summary>
    None,

    /// <summary>
    /// Assigns or unassigns the entity.
    /// </summary>
    Assign,

    /// <summary>
    /// Uses the entity.
    /// </summary>
    Use
}