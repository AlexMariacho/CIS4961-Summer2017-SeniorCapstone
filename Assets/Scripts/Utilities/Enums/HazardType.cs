/// <summary>
/// Represents the type of hazard.
/// </summary>
public enum HazardType
{
    /// <summary>
    /// Applies a force to the character.
    /// </summary>
    Force,

    /// <summary>
    /// Applies damage to the character.
    /// </summary>
    Damage,

    /// <summary>
    /// Stuns the character.
    /// </summary>
    Stun
}