/// <summary>
/// Type of ability.
/// </summary>
public enum AbilityType
{
    /// <summary>
    /// Damages a single target.
    /// </summary>
    Direct,

    /// <summary>
    /// Damages an area around a target.
    /// </summary>
    Area,

    /// <summary>
    /// Heals a target's damage.
    /// </summary>
    Heal,

    /// <summary>
    /// Shields a target from damage.
    /// </summary>
    Shield
}