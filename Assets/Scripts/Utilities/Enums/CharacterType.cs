/// <summary>
/// Represents the type of a character.
/// </summary>
public enum CharacterType
{
    /// <summary>
    /// Not a valid character type.
    /// </summary>
    None,

    /// <summary>
    /// An ally of the hero.
    /// </summary>
    Ally,

    /// <summary>
    /// An enemy of the hero.
    /// </summary>
    Enemy,

    /// <summary>
    /// A boss enemy of the hero.
    /// </summary>
    Boss,

    /// <summary>
    /// The hero.
    /// </summary>
    Hero
}