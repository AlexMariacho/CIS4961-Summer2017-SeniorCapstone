/// <summary>
/// Represents the slots in which equipment can be placed.
/// </summary>
public enum EquipmentSlot
{
    /// <summary>
    /// Equipped in right hand.
    /// </summary>
    RightHand,

    /// <summary>
    /// Equipped in left hand.
    /// </summary>
    LeftHand,

    /// <summary>
    /// Equipped using both hands.
    /// </summary>
    TwoHand,

    /// <summary>
    /// Equipped on the body.
    /// </summary>
    Body
}
