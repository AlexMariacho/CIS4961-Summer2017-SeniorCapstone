/// <summary>
/// Represents the type of a piece of equipment.
/// </summary>
public enum EquipmentType
{
    /// <summary>
    /// Used the attack.
    /// </summary>
    Weapon,

    /// <summary>
    /// Used to resist damage.
    /// </summary>
    Armor,

    /// <summary>
    /// Used to defend.
    /// </summary>
    Shield
}