/// <summary>
/// Basic attack type.
/// </summary>
public enum AttackType
{
    /// <summary>
    /// Damages a close target.
    /// </summary>
    Melee,

    /// <summary>
    /// Shoots an arrow at a target.
    /// </summary>
    Ranged,

    /// <summary>
    /// Shoots a caster ball at a target.
    /// </summary>
    Caster
}